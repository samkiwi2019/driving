    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        $this->validate($request, [
            'type' => 'required|string',
            'length' => 'integer',
        ]);

        $param = $request->only(["type", "length"]);

        if ($param['type'] != 'all') {
            $result = Quiz::with('options')->where('type', $param['type'])->inRandomOrder()->get();
        } else {
            $result = Quiz::with('options')
                ->inRandomOrder()
                ->take($param['length'])
                ->get();
        }

        return response(['data' => $result], 200);
    }

